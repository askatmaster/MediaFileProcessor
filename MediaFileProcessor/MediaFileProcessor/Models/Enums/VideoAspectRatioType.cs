    /// <summary>
    /// 5:3 aspect ratio.
    /// </summary>
    R5_3 = 2,

    /// <summary>
    /// 5:4 aspect ratio.
    /// </summary>
    R5_4 = 3,

    /// <summary>
    /// 16:9 aspect ratio.
    /// </summary>
    R16_9 = 4,

    /// <summary>
    /// 16:10 aspect ratio.
    /// </summary>
    R16_10 = 5,

    /// <summary>
    /// 17:9 aspect ratio.
    /// </summary>
    R17_9 = 6
}