namespace MediaFileProcessor.Models.Enums;

/// <summary>
/// Enum to represent different target types.
/// </summary>
public enum TargetType
{
    /// <summary>
    /// Video Compact Disc (VCD) target type.
    /// </summary>
    VCD = 0,

    /// <summary>
    /// Super Video Compact Disc (SVCD) target type.
    /// </summary>
    SVCD = 1,

    /// <summary>
    /// Digital Video Disc (DVD) target type.
    /// </summary>
    DVD = 2,

    /// <summary>
    /// Digital Video (DV) target type.
    /// </summary>
    DV = 3,

    /// <summary>
    /// DV50 target type.
    /// </summary>
    DV50 = 4
}