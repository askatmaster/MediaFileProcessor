namespace MediaFileProcessor.Models.Enums;

/// <summary>
/// Enum to represent different image formats.
/// </summary>
public enum ImageFormat
{
    /// <summary>
    /// Joint Photographic Experts Group (JPG) image format.
    /// </summary>
    JPG = 0,

    /// <summary>
    /// Portable Network Graphics (PNG) image format.
    /// </summary>
    PNG = 1,

    /// <summary>
    /// Bitmap (BMP) image format.
    /// </summary>
    BMP = 2,

    /// <summary>
    /// Tagged Image File Format (TIFF) image format.
    /// </summary>
    TIFF = 3,

    /// <summary>
    /// Joint Photographic Experts Group (JPEG) image format.
    /// </summary>
    JPEG = 4,

    /// <summary>
    /// RAW image format.
    /// </summary>
    RAW = 5,

    /// <summary>
    /// Scalable Vector Graphics (SVG) image format.
    /// </summary>
    SVG = 6,

    /// <summary>
    /// Adobe Photoshop Document (PSD) image format.
    /// </summary>
    PSD = 7
}