namespace MediaFileProcessor.Models.Enums;

/// <summary>
/// Enum to represent different video codec profile types.
/// </summary>
public enum VideoCodecProfileType
{
    /// <summary>
    /// Default profile type.
    /// </summary>
    DEFAULT = 0,

    /// <summary>
    /// Baseline profile type.
    /// </summary>
    BASELINE = 1,

    /// <summary>
    /// Main profile type.
    /// </summary>
    MAIN = 2,

    /// <summary>
    /// High profile type.
    /// </summary>
    HIGH = 3,

    /// <summary>
    /// High10 profile type.
    /// </summary>
    HIGH10 = 4,

    /// <summary>
    /// High422 profile type.
    /// </summary>
    HIGH422 = 5,

    /// <summary>
    /// High444 profile type.
    /// </summary>
    HIGH444 = 6
}